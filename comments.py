class Comment:

    def setIdxBegComment(self, idxBegComment):
        self.idxBegComment = idxBegComment

    def setLineNumberBegComment(self, lineNumberBegComment):
        self.lineNumberBegComment = lineNumberBegComment

    def setIdxEndComment(self, idxEndComment):
        self.idxEndComment = idxEndComment

    def setLineNumberEndComment(self, lineNumberEndComment):
        self.lineNumberEndComment = lineNumberEndComment

    def setCompleteComment(self, completeComment):
        self.completeComment = completeComment

    def getIdxBegComment(self):
        return self.idxBegComment

    def getIdxEndComment(self):
        return self.idxEndComment

    def getLineNumberEndComment(self):
        return self.lineNumberEndComment

    def getLineNumberBegComment(self):
        return self.lineNumberBegComment

    def getCompleteComment(self):
        return self.completeComment