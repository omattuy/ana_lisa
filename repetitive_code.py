class RepetitiveCode:

    def setFourLinesCode(self, four_lines_code):
        self.four_lines_code = four_lines_code

    def setNumberFirstLine(self, line_idx):
        self.line_idx = line_idx

    def getFourLinesCode(self):
        return self.four_lines_code

    def getNumberFirstLine(self):
        return self.line_idx